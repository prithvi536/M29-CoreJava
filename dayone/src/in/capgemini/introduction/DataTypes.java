//Programs to demonstrate datatypes
package in.capgemini.introduction;

public class DataTypes {

	public static void main(String[] args) {
/* 

integer gives values in whole number
float and double gives values in decimal points
'/' operator gives only quotient values
'%' operator gives remainder values
if we use d in double datatype it gives precision upto 16 decimal places
if we use f in double datatype it gives precision upto 7 decimal places

*/
		
		int x = 5/2;  
		float y = 103/45;
		double z = 10d/3d;
		float a = 10f/3f;
		
		System.out.println("The value of x is " + x);
		System.out.println("The value of y is " + y);
		System.out.println("The value of z is " + z);
		System.out.println("The value of a is " + a);

	}

}
